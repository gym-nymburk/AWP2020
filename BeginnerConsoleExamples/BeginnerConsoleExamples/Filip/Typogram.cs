        public static void Umeni(string input)
        {
            input = input.ToUpper();
            char[] pismena = input.ToCharArray();

            //pri nedostatecnem buffersize WriteLine zalamuje input, coz znici nase umeni
            int BufferWidth = pismena.Length * 20 + 1;
            int oBufferWidth = Console.BufferWidth;
            if (BufferWidth > oBufferWidth) Console.SetBufferSize(BufferWidth, Console.BufferHeight);

            string[] radky = new string[radkyUmeni.Length];
            int citac = 0;
            int mezery = 0;

            foreach (char pismeno in pismena)
            {                  
                //reseni pro mezeru
                if (pismeno == 32)
                {
                    for (int i = 0; i < radky.Length; i++)
                    {
                        radky[i] += "             "; //(13)
                    }
                    mezery++;
                    continue;
                }

                //odstraneni diakritiky
                char bezDiakritiky = (char)0;
                if (diakritika.Contains(pismeno))
                {
                    bezDiakritiky = bezKritika[Array.IndexOf(diakritika, pismeno)];
                }

                //pouziti umeleckeho pismena pokud pismeno bylo s diakritikou
                //foreach neumoznuje upravovat iterator, proto alternativa char bezDiakritiky
                if (bezDiakritiky != 0)
                {
                    for (int i = 0; i < radky.Length; i++)
                    {
                        radky[i] += radkyUmeni[i].Substring((bezDiakritiky - 65) * 20, 20); //utf16 UPPER abeceda zacina na 65 (stejne jako ascii), kazde umelecke pismeno je dlouhe 20 charakteru
                    }
                    citac++;
                    continue;
                }

                //znak neni podporovany (mimo A - Z)
                if (pismeno < 65 || pismeno > 90) continue;

                //pouziti umeleckeho pismena
                for(int i = 0; i < radky.Length; i++)
                {
                    radky[i] += radkyUmeni[i].Substring((pismeno - 65) * 20, 20);
                }
                citac++;
            }
            
            Console.WriteLine("\n\n");

            foreach (string radek in radky)
            {
                Console.WriteLine(radek);
            }

            //puvodni zmena buffersize pouze predpoklada ze kazde pismeno bude nahrazeno umenim aby WriteLine nezalamoval, ted diky int citac a mezery muzeme zmensit buffersize na sirku ktera je skutecne potreba
            BufferWidth = (citac * 20) + (mezery * 13) + 1;
            if (BufferWidth > oBufferWidth) Console.SetBufferSize(BufferWidth, Console.BufferHeight);
        }

